#include <iostream>

using namespace std;

int main() {
    int a, b, c, d;
    scanf("%d", &a);
    scanf("%d", &b);
    scanf("%d", &c);
    scanf("%d", &d);
    printf("DIFERENCA = %d\n", (a * b) - (c * d));
    return 0;
}
